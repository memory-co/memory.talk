"""Claude Code adapter — message classification tests.

These fixtures pin the 4-way bucket logic in ``_classify_user_message``
and the assistant-side branch of ``_parse_message``. Claude Code
transcript format evolves; if Anthropic / the CLI rename ``toolUseResult``
or change the slash-command markup, the right test below should fail
loudly instead of silently regressing 89% of ingested rounds back to
``role="human"``.

Reference: docs/report/2026-05-23-claude-code-adapter-role-misclassification.md
(if added later) and the discussion thread that produced this fix.
"""
from __future__ import annotations

import pytest

from memorytalk.adapters.claude_code import (
    ClaudeCodeAdapter, _classify_user_message,
)


# ────────── _classify_user_message ──────────


class TestClassifyUserMessage:
    """Pure unit tests over the classifier — no I/O, no schema coupling."""

    def test_tool_result_via_top_level_toolUseResult(self):
        msg = {
            "type": "user",
            "toolUseResult": {"stdout": "ok", "stderr": "", "interrupted": False},
            "message": {"content": [{"type": "tool_result", "content": "..."}]},
        }
        assert _classify_user_message(msg) == ("tool", "tool")

    def test_tool_result_via_content_type_only(self):
        # No toolUseResult field (older / partial transcript) — content
        # block type still flags it as a tool result.
        msg = {
            "type": "user",
            "message": {"content": [{"type": "tool_result", "content": "..."}]},
        }
        assert _classify_user_message(msg) == ("tool", "tool")

    def test_isMeta_caveat_classifies_as_system(self):
        msg = {
            "type": "user",
            "isMeta": True,
            "message": {"content": "<local-command-caveat>Caveat: ...</local-command-caveat>"},
        }
        assert _classify_user_message(msg) == ("system", "harness")

    def test_slash_command_invoke_str_content(self):
        msg = {
            "type": "user",
            "message": {"content": "<command-name>/model</command-name>\n<command-args>opus</command-args>"},
        }
        assert _classify_user_message(msg) == ("system", "harness")

    def test_slash_command_stdout_str_content(self):
        msg = {
            "type": "user",
            "message": {"content": "<local-command-stdout>Set model to Opus</local-command-stdout>"},
        }
        assert _classify_user_message(msg) == ("system", "harness")

    def test_local_command_caveat_without_isMeta(self):
        # Caveat text prefix is enough even when isMeta isn't set —
        # belt-and-suspenders for transcripts that lost the flag.
        msg = {
            "type": "user",
            "message": {"content": "<local-command-caveat>The messages below were generated by ...</local-command-caveat>"},
        }
        assert _classify_user_message(msg) == ("system", "harness")

    def test_interrupted_status_message(self):
        # [Request interrupted by user] is a harness-emitted status row,
        # not a user keypress.
        msg = {
            "type": "user",
            "message": {"content": [
                {"type": "text", "text": "[Request interrupted by user]"},
            ]},
        }
        assert _classify_user_message(msg) == ("system", "harness")

    def test_real_human_plain_string(self):
        msg = {
            "type": "user",
            "message": {"content": "帮我看下这个错误"},
        }
        assert _classify_user_message(msg) == ("human", "user")

    def test_real_human_text_block(self):
        msg = {
            "type": "user",
            "message": {"content": [
                {"type": "text", "text": "问题分析"},
            ]},
        }
        assert _classify_user_message(msg) == ("human", "user")

    def test_real_human_text_with_leading_whitespace(self):
        # lstrip() handles indented prefixes; "<command-name>" matters,
        # leading newlines don't.
        msg = {
            "type": "user",
            "message": {"content": "\n   <command-name>/foo</command-name>"},
        }
        assert _classify_user_message(msg) == ("system", "harness")

    def test_tool_result_wins_over_isMeta(self):
        # Edge: a row that's both tagged isMeta AND looks like a tool
        # result. tool_result is more specific — it wins.
        msg = {
            "type": "user",
            "isMeta": True,
            "toolUseResult": {"stdout": "x"},
            "message": {"content": [{"type": "tool_result", "content": "x"}]},
        }
        assert _classify_user_message(msg) == ("tool", "tool")

    def test_empty_content_falls_through_to_human(self):
        # No detectable subsignal — default to human, don't crash.
        msg = {"type": "user", "message": {"content": []}}
        assert _classify_user_message(msg) == ("human", "user")


# ────────── _parse_message integration ──────────


class TestParseMessageIntegration:
    """Quick end-to-end through the adapter — confirms classifier output
    actually lands on RoundInput, and assistant path is untouched."""

    def setup_method(self):
        self.adapter = ClaudeCodeAdapter()

    def test_assistant_text_unaffected(self):
        msg = {
            "type": "assistant",
            "uuid": "u1",
            "message": {"content": [{"type": "text", "text": "ok"}]},
        }
        out = self.adapter._parse_message(msg)
        assert out is not None
        assert out.role == "assistant"
        assert out.speaker == "assistant"

    def test_assistant_with_tool_use_block(self):
        # Assistant initiating a tool call. Still role=assistant.
        msg = {
            "type": "assistant",
            "uuid": "u2",
            "message": {"content": [{
                "type": "tool_use", "name": "Bash", "input": {"command": "ls"},
            }]},
        }
        out = self.adapter._parse_message(msg)
        assert out is not None
        assert out.role == "assistant"

    def test_user_tool_result_lands_as_tool_role(self):
        msg = {
            "type": "user",
            "uuid": "u3",
            "toolUseResult": {"stdout": "ok"},
            "message": {"content": [{"type": "tool_result", "content": "ok"}]},
        }
        out = self.adapter._parse_message(msg)
        assert out is not None
        assert out.role == "tool"
        assert out.speaker == "tool"

    def test_user_slash_command_lands_as_system_role(self):
        msg = {
            "type": "user",
            "uuid": "u4",
            "message": {"content": "<command-name>/model</command-name>"},
        }
        out = self.adapter._parse_message(msg)
        assert out is not None
        assert out.role == "system"
        assert out.speaker == "harness"

    def test_user_real_input_lands_as_human_role(self):
        msg = {
            "type": "user",
            "uuid": "u5",
            "message": {"content": "请帮我分析这个 bug"},
        }
        out = self.adapter._parse_message(msg)
        assert out is not None
        assert out.role == "human"
        assert out.speaker == "user"
